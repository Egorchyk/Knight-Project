package Ammunitions;

public class Ammunition {
    private String name;
    private int price;
    private int weight;

    public Ammunition(String name, int price, int weight) {
        this.name = name;
        this.price = price;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public int getWeight() {
        return weight;
    }
}
